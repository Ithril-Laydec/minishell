#include "../../libft.h"

char	*ft_strdup(const char *s)
{
	char	*new_str;

	new_str = (char *)malloc(sizeof(char) * (ft_strlen((char *)s) + 1));
	if (new_str == NULL)
		return (NULL);
	ft_strlcpy(new_str, s, ft_strlen((char *)s) + 1);
	return (new_str);
}
